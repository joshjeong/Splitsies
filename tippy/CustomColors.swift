import UIKit

class CustomColors: UIColor {
    
    func darkBlue() -> UIColor {
            return "#5d9efb".hexColor
    }
    
    func lightBlue() -> UIColor {
        return "#0096FF".hexColor
    }
    
    func turquoise() -> UIColor {
        return "#72CBCF".hexColor
    }
    
    func salmon() -> UIColor {
        return "#FD7160".hexColor
    }
}
